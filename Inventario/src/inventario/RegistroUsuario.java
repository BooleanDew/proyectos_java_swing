    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String clave = txtClave.getText();
        // 1=activo, 0=inactivo
        int estado = 1;
        Usuario usuario = new Usuario(nombre, apellido, clave, estado);
        int resultado = usuario.setCrear();
        if (!(nombre.equals("")) || !(apellido.equals("")) || !(clave.equals(""))) {
            if (resultado == 1) {
                JOptionPane.showMessageDialog(null, "Usuario registrado con exito");
            }
        } else {
            //txtNombre.setBackground(Color.red);
            JOptionPane.showMessageDialog(this, "Favor complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

